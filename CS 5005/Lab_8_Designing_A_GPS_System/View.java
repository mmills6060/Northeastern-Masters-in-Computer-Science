// View.java
public class View {
    public void displayDistance(double distance) {
        // Display distance to the user
        System.out.println("Distance: " + distance + " miles");
    }

    public void displayTime(double time) {
        // Display time to the user
        System.out.println("Time: " + time + " hours");
    }

    public void displayTimeToRefuel(double timeToRefuel) {
        // Display time remaining until refueling to the user
        System.out.println("Time to refuel: " + timeToRefuel + " hours");
    }

    public void displayStoppingTime(double stoppingTime) {
        // Display stopping time to the user
        System.out.println("Stopping time: " + stoppingTime + " seconds");
    }
}
