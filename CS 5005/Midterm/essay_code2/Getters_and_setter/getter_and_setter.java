package essay_code2.Getters_and_setter;
public class getter_and_setter {
    
    // instance variables
    private String name;
    private int age;
    
    // getter and setter methods
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public int getAge() {
        return age;
    }
    
    public void setAge(int age) {
        this.age = age;
    }
} 