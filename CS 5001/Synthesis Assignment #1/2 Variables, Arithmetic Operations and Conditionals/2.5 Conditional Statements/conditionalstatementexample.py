# An example of a conditional statement

x = 2
if x > 0:
    print("x is positive")
else:
    print("x is not positive")


# another example of a conditional statement

animal = "dog"

if animal == "dog":
    print("This is a dog.")
elif animal == "cat":
    print("This is a cat.")
elif animal == "horse":
    print("This is a horse.")
else:
    print("I don't know what kind of animal this is.")

