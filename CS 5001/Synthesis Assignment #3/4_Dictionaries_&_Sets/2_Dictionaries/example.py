# An example of a dictionary

# Create a dictionary of tennis players and their rankings
rankings = {
    "Roger Federer": 1,
    "Rafael Nadal": 2,
    "Novak Djokovic": 3,
    "Dominic Thiem": 4
}

# Retrieve the ranking of "Roger Federer" from the dictionary
federer_ranking = rankings["Roger Federer"]
print(federer_ranking)


