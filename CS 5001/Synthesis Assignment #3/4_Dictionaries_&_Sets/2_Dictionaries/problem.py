# An example of a dictionary

# Create a dictionary of ski mountains in maine and their vertical drop
ski_mountains = {
    "Sugarloaf": 2000,
    "Sunday River": 2000,
    "Saddleback": 1000,
    "Shawnee Peak": 1000
}

# Retrieve the vertical drop of "Sugarloaf" from the dictionary
sugarloaf_vertical_drop = ski_mountains["Sugarloaf"]
print(sugarloaf_vertical_drop)


