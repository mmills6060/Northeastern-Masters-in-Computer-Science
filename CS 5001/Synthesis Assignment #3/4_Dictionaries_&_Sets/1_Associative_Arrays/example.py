# An example of an associative array

# Create an associative array of student names and their grades
grades = {
    "John": 85,
    "Bob": 92,
    "David": 78,
    "Bruce": 89
}

# Retrieve the grade of "Charlie" from the array
charlie_grade = grades["Charlie"]
print(charlie_grade) 

# Output: 78

